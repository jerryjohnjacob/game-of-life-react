  getNeighbours: function(rowNumber, columnNumber) {
    return([
      this.getCell(rowNumber, columnNumber - 1),
      this.getCell(rowNumber, columnNumber + 1),
      this.getCell(rowNumber - 1, columnNumber - 1),
      this.getCell(rowNumber - 1, columnNumber),
      this.getCell(rowNumber + 1, columnNumber),
      this.getCell(rowNumber - 1, columnNumber + 1),
      this.getCell(rowNumber + 1, columnNumber - 1),
      this.getCell(rowNumber + 1, columnNumber + 1)
    ]);
  },
  getCell: function(rowNumber, columnNumber) {
    var row = this.state.world[rowNumber];
    if(row !== undefined) {
      return(this.state.world[rowNumber][columnNumber]);
    } else {
      return 0;
    }
  },
