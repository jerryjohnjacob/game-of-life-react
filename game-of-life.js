var Cell = React.createClass({
  render: function() {
    var cellClass = classNames("cell", {
      'alive': this.isAlive()
    });
    return (
        <div className={cellClass}></div>
    );
  },
  componentDidMount: function() {
    this.setState({alive: this.props.alive});
  },
  isAlive: function() {
    return(this.props.alive === 1);
  }
});

